let gradePercent = 90;
switch (true) {
  case (gradePercent >= 90):
    console.log(`Grade is A`);
    break;
  case (gradePercent >= 80):
    console.log(`Grade is B`);
    break;
  case (gradePercent >= 70):
    console.log(`Grade is C`);
    break;
  case (gradePercent >= 60):
    console.log(`Grade is D`);
    break;
  default:
    console.log(`Grade is F`);
    break;
}

